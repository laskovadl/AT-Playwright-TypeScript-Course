// Exercies 1: String Concatenation and Arithmetic Operations
// Define variables
let str: string = "Hello! I am Petar Petrov,";
let age: number = 20;
let str2: string = "years old.";

// Print the concatenated string
console.log(str + " " + age + " " + str2);

// Exercise 2: Arithmetic Operations
// Define two numbers
let num1: number = 0;
let num2: number = 5;

// Calculate addition and subtraction
let sum: number = num1 + num2;
let difference: number = num1 - num2;

// Print the results
console.log("Addition:", sum);
console.log("Subtraction:", difference);

// Exercise 3: Range Check
// Declare a numeric variable and assign a number
let num: number = 20;

// Define the range
let min: number = 10;
let max: number = 19;

// Declare a boolean variable to check if num is within the range (inclusive)
let isWithinRange: boolean = num >= min && num <= max;

// Print the result
console.log("The given number:", num);
console.log("is within range:", isWithinRange);